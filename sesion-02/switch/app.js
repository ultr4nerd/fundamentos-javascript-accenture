const alimento = "pizza";

switch (alimento) {
  case "manzana":
    console.log("Esto es una manzana");
    break;
  case "pizza":
    console.log("Esto es una pizza");
    break;
  case "pera":
    console.log("Esto es una pera");
    break;
  default:
    console.log("Desconozco este alimento");
}
